private String getWeatherData(String kotaNama) {
    String apiKey = "589e7fc60fe84ccb9be01d4c426455e1"; // API Key Saya
    String apiUrl = "http://api.openweathermap.org/data/2.5/weather?q=" + kotaNama + "&appid=" + apiKey + "&units=metric";
    StringBuilder response = new StringBuilder();
    
    try {
        URL url = new URL(apiUrl);
        HttpURLConnection conn = (HttpURLConnection) url.openConnection();
        conn.setRequestMethod("GET");

        try (BufferedReader reader = new BufferedReader(new InputStreamReader(conn.getInputStream()))) {
            String line;
            while ((line = reader.readLine()) != null) {
                response.append(line);
            }
        }
        conn.disconnect();
    } catch (IOException e) {
    }
    
    return response.toString();
}

private void displayWeather(String kotaNama) {
    try {
        String jsonData = getWeatherData(kotaNama);
        if (jsonData != null && !jsonData.isEmpty()) {
            JSONObject jsonObject = new JSONObject(jsonData);
            
            // Extract weather information
            String city = jsonObject.getString("name");
            double temp = jsonObject.getJSONObject("main").getDouble("temp");
            int humidity = jsonObject.getJSONObject("main").getInt("humidity"); // Extract humidity
            String description = jsonObject.getJSONArray("weather").getJSONObject(0).getString("description");
            String iconCode = jsonObject.getJSONArray("weather").getJSONObject(0).getString("icon");
            double windSpeed = jsonObject.getJSONObject("wind").getDouble("speed");
            
            // Update GUI components
            lblCuaca.setText(city + ": " + description);
            lblSuhu.setText("Temperature: " + temp + "Â°C");
            lblAngin.setText("Wind Speed: " + windSpeed + " m/s");
            lblKelembapan.setText("Kelembapan: " + humidity + " %");
            
            // Fetch and set the weather icon
            String iconUrl = "http://openweathermap.org/img/wn/" + iconCode + ".png";
            URL url = new URL(iconUrl);
            ImageIcon icon = new ImageIcon(url);
            icoCuaca.setIcon(new ImageIcon(icon.getImage().getScaledInstance(100, 100, Image.SCALE_SMOOTH)));
        } else {
            lblCuaca.setText("Error fetching data");
            lblSuhu.setText("");
            lblAngin.setText("");
            icoCuaca.setIcon(null);
        }
    } catch (MalformedURLException | JSONException e) {
    }
}
